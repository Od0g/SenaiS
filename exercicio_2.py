# Programa de Mutiplicação de três Numeros 

numero_1 = int(input("Digite o Primeiro Numero: "))
numero_2= int(input("Digite o Segundo Numero: "))
numero_3 = int(input("Digite o Terceiro Numero: "))

Multiplicação = numero_1*numero_2*numero_3 

print(f" A multiplicação entre {numero_1}, {numero_2} e {numero_3} é {Multiplicação} ")